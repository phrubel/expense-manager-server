// user model
const userModel = require('../models/userModel');

// login controller
const loginController = async (req, res) => {
  try {
    const { email, password } = req.body;
    const user = await userModel.findOne({ email, password });
    if (!user) {
      return res.status(404).send('user not found');
    }
    res.status(200).json({
      success: true,
      user,
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      error,
    });
  }
};

// reg controller
const regController = () => {};

// exports
module.exports = { loginController, regController };
